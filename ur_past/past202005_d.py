n = int(input())
s = [str(input()) for _ in range(5)]

number = ['.###..#..###.###.#.#.###.###.###.###.###.',
          '.#.#.##....#...#.#.#.#...#.....#.#.#.#.#.',
          '.#.#..#..###.###.###.###.###...#.###.###.',
          '.#.#..#..#.....#...#...#.#.#...#.#.#...#.',
          '.###.###.###.###...#.###.###...#.###.###.']

ans = []
for i in range(n):
    j = 4 * i + 1
    for num in range(10):
        if all(s[k][j:j + 4] == number[k][4 * num + 1:4 * num + 5] for k in range(5)):
            ans.append(num)
print(*ans, sep='')
